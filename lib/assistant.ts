/**
 * Безключова «Олександра» — працює без жодного AI-API.
 * Розпізнає типові наміри й відповідає готовими репліками з бази знань.
 * `telegram: true` означає, що момент доречний для передачі в Telegram
 * (ціна для конкретної моделі, готовність замовити, незрозуміле питання).
 *
 * Якщо на Vercel заданий ANTHROPIC_API_KEY — натомість працює повноцінний
 * живий AI (див. app/api/chat/route.ts). Цей модуль — надійний fallback.
 */

export interface RuleResult {
  reply: string;
  telegram: boolean;
}

const norm = (s: string) =>
  s.toLowerCase().replace(/['’`ʼ]/g, '').replace(/\s+/g, ' ').trim();

const has = (t: string, ...keys: string[]) => keys.some((k) => t.includes(k));

export function ruleReply(input: string): RuleResult {
  const t = norm(input);

  if (!t) {
    return {
      reply:
        'Вітаю! 👋 Я Олександра, консультант MobiDoctor. Що сталося з телефоном? Підкажу ціну, строки та оформлю безкоштовний виїзд кур’єра по Харкову.',
      telegram: false,
    };
  }

  // 1) Готовність замовити / викликати кур'єра
  if (has(t, 'замов', 'виклич', 'виклич', 'оформ', 'заберіть', 'приїд', 'запиш', 'залишити заявку', 'лишити заявку', 'хочу ремонт', 'хочу полагод', 'записатися'))
    return {
      reply:
        'Чудово! 🚚 Залиште, будь ласка, ім’я та номер телефону — кур’єр безкоштовно приїде у зручний час. Або напишіть нам прямо в Telegram, менеджер швидко відповість.',
      telegram: true,
    };

  // 2) Діагностика (раніше за «ціну», бо вона безкоштовна)
  if (has(t, 'діагност'))
    return {
      reply:
        'Діагностика безкоштовна, як і виїзд кур’єра по Харкову. Якщо відмовитесь від ремонту — нічого не платите.',
      telegram: false,
    };

  // 3) Виїзд / кур'єр
  if (has(t, 'виїзд', 'кур’єр', 'курєр', 'кур', 'забере телефон', 'доставк', 'приїде'))
    return {
      reply:
        'Виїзд кур’єра по всьому Харкову — безкоштовний. Кур’єр забере телефон у зручний час, ми відремонтуємо й привеземо назад готовий.',
      telegram: false,
    };

  // 4) Гарантія
  if (has(t, 'гарант'))
    return {
      reply: 'Гарантія до 12 місяців на роботи та встановлені деталі, з гарантійним талоном.',
      telegram: false,
    };

  // 5) Строки
  if (has(t, 'скільки час', 'як довго', 'скільки трив', 'за скільки', 'коли готов', 'термін', 'строк', 'швидко зробите'))
    return {
      reply:
        'Типові ремонти (екран, акумулятор) — 30–60 хвилин. Складніші (материнська плата, після води) — від кількох годин.',
      telegram: false,
    };

  // 6) Запчастини
  if (has(t, 'оригінал', 'копі', 'запчаст', 'деталь', 'детал'))
    return {
      reply:
        'Деталі на вибір: оригінал або сертифікована копія — підкажемо оптимальний варіант під ваш бюджет. Тип деталі узгоджуємо до ремонту.',
      telegram: false,
    };

  // 7) Після води
  if (has(t, 'вод', 'залив', 'залит', 'утопив', 'рідин', 'намок'))
    return {
      reply:
        'Відновлюємо телефони після води: чистимо плату від окислів і просушуємо — від 900 грн. Точніше скажемо після безкоштовної діагностики.',
      telegram: false,
    };

  // 8) Зарядка / роз'єм (перед акумулятором — «не заряджається»)
  if (has(t, 'не заряджа', 'розєм зарядки', 'роз’єм зарядки', 'порт зарядки', 'type-c', 'lightning', 'гніздо зарядки'))
    return {
      reply:
        'Ремонт роз’єму зарядки — від 650 грн. Кур’єр забере телефон безкоштовно на діагностику, і ми точно визначимо причину.',
      telegram: false,
    };

  // 9) Акумулятор
  if (has(t, 'акум', 'батар', 'не трим', 'тримає заряд', 'швидко сід', 'сідає', 'здувся', 'розряджа'))
    return {
      reply: 'Заміна акумулятора — від 700 грн, зазвичай 30–60 хвилин. Телефон знову триматиме заряд цілий день.',
      telegram: false,
    };

  // 10) Екран / дисплей
  if (has(t, 'екран', 'дисплей', 'розбив', 'розбит', 'тріснув', 'тріщин', 'скло', 'матриц', 'смуги', 'не працює сенсор'))
    return {
      reply:
        'Заміна екрана: iPhone — від 1200 грн, Samsung — від 1500 грн, Xiaomi/Poco — від 900 грн. Точну ціну для вашої моделі підкажемо після безкоштовної діагностики.',
      telegram: false,
    };

  // 11) Дані / безпека
  if (has(t, 'дан', 'безпек', 'конфіденц', 'розблок'))
    return {
      reply: 'Ваші дані в безпеці. За бажанням ремонтуємо без розблокування телефону.',
      telegram: false,
    };

  // 12) Ціна / прайс (для точної ціни моделі — у Telegram)
  if (has(t, 'ціна', 'цін', 'скільки кошт', 'скільки буде', 'вартіст', 'прайс', 'почому', 'по чому', 'коштує', 'дорого'))
    return {
      reply:
        'Орієнтовно: заміна екрана від 900–1500 грн, акумулятор від 700 грн, роз’єм зарядки від 650 грн, після води від 900 грн. Точну ціну саме для вашої моделі назвемо після безкоштовної діагностики — або напишіть у Telegram, менеджер підкаже одразу.',
      telegram: true,
    };

  // 13) Бренд без конкретної поломки
  if (has(t, 'samsung', 'самсунг', 'xiaomi', 'ксіом', 'redmi', 'редмі', 'poco', 'поко', 'realme', 'реалмі', 'iphone', 'айфон', 'apple', 'oneplus', 'honor', 'huawei'))
    return {
      reply:
        'Так, ремонтуємо! Екран, акумулятор, зарядку, камеру, після води та інше. Яка саме поломка? Виїзд кур’єра по Харкову безкоштовний.',
      telegram: false,
    };

  // 14) Привітання / подяка
  if (has(t, 'привіт', 'вітаю', 'добрий', 'доброго', 'здраст', 'hello', 'привет', 'хай'))
    return {
      reply:
        'Вітаю! 👋 Чим можу допомогти? Підкажу ціну, строки ремонту або оформлю безкоштовний виїзд кур’єра.',
      telegram: false,
    };
  if (has(t, 'дякую', 'спасиб', 'дяки', 'вдячн'))
    return { reply: 'Будь ласка! 😊 Якщо виникнуть питання — пишіть, я на зв’язку.', telegram: false };

  // 15) Fallback — м'яко веди в Telegram
  return {
    reply:
      'Підкажу з ремонту смартфонів: виїзд, ціни, гарантія, строки. Опишіть, будь ласка, модель і що сталося. Для точної ціни та замовлення зручно написати нам у Telegram.',
    telegram: true,
  };
}
